// Lattice Backend (stub — Phase 3)
// ════════════════════════════════════════════

/// Lattice ZK proof backend (Phase 3).
///
/// Will implement:
///   Range: lattice-based bulletproof analog (Esgin et al.)
///   Balance: MLWE-based Σ-protocol for commitment arithmetic
///
/// Currently returns BackendUnavailable for all operations.
pub struct LatticeBackend;

impl LatticeBackend {
    pub const BACKEND_ID: u8 = 0x02;
}

impl RangeProofBackend for LatticeBackend {
    fn backend_id(&self) -> u8 { Self::BACKEND_ID }

    fn prove(&self, _opening: &CommitmentOpening) -> Result<RangeProofData, ProofError> {
        Err(ProofError::BackendUnavailable(
            "Lattice range proofs not yet implemented (Phase 3)".into()
        ))
    }

    fn verify(&self, _hash: &[u8; 32], _proof: &RangeProofData) -> Result<bool, ProofError> {
        Err(ProofError::BackendUnavailable(
            "Lattice range proofs not yet implemented (Phase 3)".into()
        ))
    }
}

impl BalanceProofBackend for LatticeBackend {
    fn backend_id(&self) -> u8 { Self::BACKEND_ID }

    fn prove(&self, _i: &[CommitmentOpening], _o: &[CommitmentOpening], _f: u64) -> Result<BalanceProofData, ProofError> {
        Err(ProofError::BackendUnavailable(
            "Lattice balance proofs not yet implemented (Phase 3)".into()
        ))
    }

    fn verify(&self, _proof: &BalanceProofData) -> Result<bool, ProofError> {
        Err(ProofError::BackendUnavailable(
            "Lattice balance proofs not yet implemented (Phase 3)".into()
        ))
    }
}

// ════════════════════════════════════════════
// Unified backend (prevents mismatched range/balance backends)
// ════════════════════════════════════════════

/// Combined proof backend.
///
/// A single type must implement BOTH range and balance proofs.
/// This makes it structurally impossible to use different backends
/// for range vs balance within the same transaction.
///
/// The builder and verifier accept `&impl ProofBackend`.
pub trait ProofBackend: RangeProofBackend + BalanceProofBackend {}

// Auto-implement for any type that has both traits
impl<T: RangeProofBackend + BalanceProofBackend> ProofBackend for T {}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::commitment;

    #[test]
    fn test_testnet_range_proof() {
        let backend = TestnetBackend;
        let opening = commitment::commit(42_000, 99);
        let proof = backend.prove(&opening).unwrap();
        assert_eq!(proof.backend_id, TestnetBackend::BACKEND_ID);
        assert!(backend.verify(&opening.hash, &proof).unwrap());
    }

    #[test]
    fn test_testnet_balance_proof() {
        let backend = TestnetBackend;
        let inp = commitment::commit(1000, 10);
        let out = commitment::commit(900, 10);
        let proof = backend.prove(&[inp], &[out], 100).unwrap();
        assert!(backend.verify(&proof).unwrap());
    }

    #[test]
    fn test_testnet_balance_unbalanced_fails() {
        let backend = TestnetBackend;
        let inp = commitment::commit(1000, 10);
        let out = commitment::commit(500, 10); // short 400
        let proof = backend.prove(&[inp], &[out], 100).unwrap();
        assert!(!backend.verify(&proof).unwrap());
    }

    #[test]
    fn test_lattice_backend_unavailable() {
        let backend = LatticeBackend;
        let opening = commitment::commit(100, 10);
        assert!(backend.prove(&opening).is_err());
    }
}
